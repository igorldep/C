{
    float num = 0;

    do
    {
        printf ("\nDigite um número inteiro e positivo: ");
        scanf("%f", &num);
    }
    while ( ( num < 0 ) || ( num == 0 ) );

    printf ("\nO número %f é inteiro e positivo!\n", num);
    printf ("\nFim do programa\n\n");

    system("pause");
    return(0);
}

